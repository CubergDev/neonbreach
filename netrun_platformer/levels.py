from __future__ import annotations

from dataclasses import dataclass


@dataclass(frozen=True)
class LevelSpec:
    name: str
    objective: str
    briefing: str
    sky_top: tuple[int, int, int]
    sky_bottom: tuple[int, int, int]
    rows: list[str]
    mechanic: str = "standard"
    mechanic_hint: str = ""


def _rows(raw: str) -> list[str]:
    rows = [line.rstrip() for line in raw.strip("\n").splitlines()]
    width = max(len(row) for row in rows)
    normalized = [row.ljust(width, ".") for row in rows]
    while width > 1 and all(row[width - 1] == "." for row in normalized):
        width -= 1
    return [row[:width] for row in normalized]


